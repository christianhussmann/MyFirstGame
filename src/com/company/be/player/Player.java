package com.company.be.player;

public class Player {
    private int skillLevel;
    private int exp;

    public int getSkillLevel() {
        return skillLevel;
    }

    public void setLevel(int skillLevel) {
        this.skillLevel = skillLevel;
    }

    public int getExp() {
        return exp;
    }

    public void setExp(int exp) {
        this.exp = exp;
    }
}
